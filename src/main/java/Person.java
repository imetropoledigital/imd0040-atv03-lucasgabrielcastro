public class Person {
    
    // fields
    private String Name;
    private String Hometown;

    // constructor(s)
    public Person(string name, string hometown)
    {
        this.Name = name;
        this.Hometown = hometown;
    }

    // methods
    public String hometown()
    {
        return this.Hometown;
    }

    public String name()
    {
        return this.Name;
    }
}
